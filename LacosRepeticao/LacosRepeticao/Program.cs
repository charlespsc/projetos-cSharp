class Program
{
    static void Main(string[] args)
    {
        // Estrutura de Repetição
  
        // Foreach

        // Do (while)

        // While
        int numero = 0;

        while (numero <= 10)
        {
            Console.WriteLine(numero);
            numero++;
        }

        // For
        for (int i = 0; i <= 10; i++)
        {
            Console.WriteLine(i);
        }

    }
}